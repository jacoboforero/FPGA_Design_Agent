module TopModule (
    input  [3:0] x,
    output       f
);

// Karnaugh map (x[3:2] = row, x[1:0] = col):
//  x[3:2] | x[1:0]=00 | 01 | 11 | 10
//   00    |   1      | 0  | 0  | 1
//   01    |   0      | 0  | 0  | 0
//   11    |   1      | 1  | 1  | 0
//   10    |   1      | 1  | 0  | 1

assign f =
    // 00 00: 1
    (~x[3] & ~x[2] & ~x[1] & ~x[0]) |
    // 00 10: 1
    (~x[3] & ~x[2] &  x[1] & ~x[0]) |
    // 10 00: 1
    ( x[3] & ~x[2] & ~x[1] & ~x[0]) |
    // 10 01: 1
    ( x[3] & ~x[2] & ~x[1] &  x[0]) |
    // 10 11: 1
    ( x[3] & ~x[2] &  x[1] &  x[0]) |
    // 11 00: 1
    ( x[3] &  x[2] & ~x[1] & ~x[0]) |
    // 11 01: 1
    ( x[3] &  x[2] & ~x[1] &  x[0]) |
    // 11 10: 0 (skip)
    // 11 11: 1
    ( x[3] &  x[2] &  x[1] &  x[0]);

endmodule