module TopModule (
    input  [3:0] x,
    output       f
);

assign f =
    // x[3:2]=01, x[1:0]=10: f=0
    ((x[3:2] == 2'b01) && (x[1:0] == 2'b10)) ? 1'b0 :
    // x[3:2]=01, x[1:0]=00: f=0
    ((x[3:2] == 2'b01) && (x[1:0] == 2'b00)) ? 1'b0 :
    // x[3:2]=01, x[1:0]=11: f=1
    ((x[3:2] == 2'b01) && (x[1:0] == 2'b11)) ? 1'b1 :
    // x[3:2]=01, x[1:0]=01: f=0
    ((x[3:2] == 2'b01) && (x[1:0] == 2'b01)) ? 1'b0 :
    // x[3:2]=11, x[1:0]=00: f=1
    ((x[3:2] == 2'b11) && (x[1:0] == 2'b00)) ? 1'b1 :
    // x[3:2]=11, x[1:0]=01: f=1
    ((x[3:2] == 2'b11) && (x[1:0] == 2'b01)) ? 1'b1 :
    // x[3:2]=10, x[1:0]=00: f=1
    ((x[3:2] == 2'b10) && (x[1:0] == 2'b00)) ? 1'b1 :
    // x[3:2]=10, x[1:0]=01: f=1
    ((x[3:2] == 2'b10) && (x[1:0] == 2'b01)) ? 1'b1 :
    // x[3:2]=10, x[1:0]=10: f=0
    ((x[3:2] == 2'b10) && (x[1:0] == 2'b10)) ? 1'b0 :
    // x[3:2]=00, x[1:0]=01: f=0
    ((x[3:2] == 2'b00) && (x[1:0] == 2'b01)) ? 1'b0 :
    // x[3:2]=11, x[1:0]=10: f=don't care (assign 0)
    // x[3:2]=11, x[1:0]=11: f=don't care (assign 0)
    // x[3:2]=10, x[1:0]=11: f=don't care (assign 0)
    // x[3:2]=00, x[1:0]=00: f=don't care (assign 0)
    // x[3:2]=00, x[1:0]=10: f=don't care (assign 0)
    1'b0;

endmodule